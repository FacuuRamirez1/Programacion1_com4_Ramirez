# 1) Calcular factorial de un número y los que se encuentran dentro:
print("===============")
print("  Ejercicio 1  ")
print("===============")

def factorial(num):
    if num == 0:
        return 1
    else:
        return num * factorial(num - 1)

num = int(input("Ingrese un número entero positivo: "))
for i in range(1, num + 1): 
    print(f'El factorial de {i} es: {factorial(i)}')

